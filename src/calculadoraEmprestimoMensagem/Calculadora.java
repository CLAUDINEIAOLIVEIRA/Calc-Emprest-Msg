package calculadoraEmprestimoMensagem;

public class Calculadora {

  public static void soma(double num1, double num2) {
	double resultado = num1 + num2;
	System.out.println(" A SOMA de "  +num1+  "mais"    +num2+  "será:"  + resultado);

}
  
   public static void subtracao(double num1, double num2) {
	double resultado = num1 - num2;
	System.out.println("A SUBTRAÇÃO"  +num1+  "mais"   +num2+  "será:"  +resultado);
}
   
  
     public static void multiplicacao(double num1, double num2) {
	double resultado = num1 * num2;
	System.out.println("A MULTIPLICAÇÃO"  +num1+  "mais"   +num2+ "será:"  + resultado);
	
}
   
  
   public static void divisao(double num1, double num2) {
	double resultado= num1 / num2;
	System.out.println("A DIVISÃO"  +num1+ "mais" +num2+ "será:" + resultado);
	  
}
  
  
  
}
  

